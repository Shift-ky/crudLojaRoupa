    public void listaClientes(){
        try {
            ClienteDao clienteDao = new ClienteDao();
            
            DefaultTableModel model = (DefaultTableModel) tabelaView.getModel();
            model.setNumRows(0);
            
            ArrayList<Cliente> lista = new ArrayList<>();
            
            lista = clienteDao.readyCliente();
            
            for(int i = 0; i < lista.size(); i ++){
                
                model.addRow(new Object[]{
                    lista.get(i).getNome(),
                    lista.get(i).getTelefone(),
                    lista.get(i).getEmail(),
                    lista.get(i).getCpf()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Listar aluno view" + e);
        }
        
    }
    public void limparCliente(){
        campoNome.setText("");
        campoCpf.setText("");
        campoEmail.setText("");
        campoTelefone.setText("");
        campoNome.requestFocus();
    }
    
    public void cadastraClienete(){
        Cliente cliente = new Cliente();
        String nome,telefone,cpf,email;
        
        nome = campoNome.getText();
        telefone = campoTelefone.getText();
        cpf = campoCpf.getText();
        email = campoEmail.getText();
        
        cliente.setNome(nome);
        cliente.setTelefone(telefone);
        cliente.setCpf(cpf);
        cliente.setEmail(email);
        
        ClienteDao clienteDao = new ClienteDao();
        clienteDao.criarCliente(cliente);
       
    }

